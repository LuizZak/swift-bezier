/// A typealias for `QuadBezier2` that constraints its point type to `Bezier2DPoint`,
/// defining a quadratic Bézier with double-precision floating-point coordinate type.
public typealias QuadBezier2D = QuadBezier2<Bezier2DPoint>

/// A typealias for `QuadBezier2` that constraints its point type to `Bezier2FPoint`,
/// defining a quadratic Bézier with single-precision floating-point coordinate type.
public typealias QuadBezier2F = QuadBezier2<Bezier2FPoint>
